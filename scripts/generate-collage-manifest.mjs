import fs from 'node:fs/promises';
import path from 'node:path';

const COLLAGE_DIR = path.join(process.cwd(), 'images', 'collage');
const OUT_FILE = path.join(COLLAGE_DIR, 'manifest.js');

const IMAGE_EXTS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif']);

function isImageFile(fileName) {
  const ext = path.extname(fileName).toLowerCase();
  return IMAGE_EXTS.has(ext);
}

function toCollageUrl(fileName) {
  return `images/collage/${fileName}`;
}

async function main() {
  const entries = await fs.readdir(COLLAGE_DIR, { withFileTypes: true });

  const fileNames = entries
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => name !== 'manifest.js')
    .filter(isImageFile)
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }));

  const urls = fileNames.map(toCollageUrl);

  const output =
    `// Auto-generated by scripts/generate-collage-manifest.mjs\n` +
    `// Do not edit by hand.\n` +
    `window.__COLLAGE_IMAGES__ = ${JSON.stringify(urls, null, 2)};\n`;

  await fs.writeFile(OUT_FILE, output, 'utf8');

  // eslint-disable-next-line no-console
  console.log(`Wrote ${urls.length} images to ${path.relative(process.cwd(), OUT_FILE)}`);
}

main().catch((err) => {
  // eslint-disable-next-line no-console
  console.error(err);
  process.exitCode = 1;
});
